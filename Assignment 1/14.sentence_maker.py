'''Take the sentence:
Python is incredible. I love it!.
Store each word in a separate variable, then print out the sentence on one line using print function'''

def sentence_maker():
    word1 = 'Python'
    word2 = 'is'
    word3 = 'incredible.'
    word4 = 'I'
    word5 = 'love'
    word6 = 'it!'
    print(word1, word2, word3, word4, word5, word6)

sentence_maker()
